// Helper to parse DD.MM.YYYY HH:mm
const parseCustomDate = (dateStr: string): number => {
  try {
    const [d, t] = dateStr.split(' ');
    if (!d || !t) return 0;
    
    const [day, month, year] = d.split('.').map(Number);
    const [hour, minute] = t.split(':').map(Number);
    
    // Note: Month is 0-indexed in JS Date
    return new Date(year, month - 1, day, hour, minute).getTime();
  } catch (e) {
    return 0;
  }
};

export const parseRawData = (rawData: string): DataPoint[] => {
  return rawData.split('\n')
    .map(line => {
      const [dateStr, visitorsStr] = line.split('\t');
      if (!dateStr) return null;
      
      const timestamp = parseCustomDate(dateStr);
      const visitors = visitorsStr ? parseInt(visitorsStr.trim(), 10) : 0;
      
      if (!timestamp || isNaN(visitors)) return null;

      return {
        timestamp,
        visitors,
        maxCapacity: 300 // Updated default capacity
      };
    })
    .filter((d): d is DataPoint => d !== null)
    .sort((a, b) => a.timestamp - b.timestamp);
};
